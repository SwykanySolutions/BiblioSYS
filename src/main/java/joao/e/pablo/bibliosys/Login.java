    private void ButtonVisibilidadeSenhaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonVisibilidadeSenhaMouseClicked
        // TODO add your handling code here:
        if (InputPassword.getEchoChar() == 0) {
            // Se a senha j√° estiver vis√≠vel, oculta novamente
            InputPassword.setEchoChar('\u2022');
        } else {
            // Se a senha estiver oculta, torna vis√≠vel
            InputPassword.setEchoChar((char) 0);
        }
    }//GEN-LAST:event_ButtonVisibilidadeSenhaMouseClicked

    private void ButtonLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonLoginMouseClicked
        // TODO add your handling code here:
        String Login = InputLogin.getText();
        String Senha = String.valueOf(InputPassword.getPassword());
        if("".equals(Login) || Login == null){
            JOptionPane.showMessageDialog(null, "O Login n√£o pode ser v√°zio", "Erro", JOptionPane.ERROR_MESSAGE );
            InputLogin.requestFocus();
            return;
        }
        if("".equals(Senha) || Senha == null){
            JOptionPane.showMessageDialog(null, "A Senha n√£o pode ser v√°zia", "Erro", JOptionPane.ERROR_MESSAGE );
            InputPassword.requestFocus();
            return;
        }
        
        try {
            UserLogin.Login(Login, Senha);
            Boolean retorno = Session.getBoolean("login_status");
            if(!retorno){
                JOptionPane.showMessageDialog(PanelFormLogin, "N√£o foi opss√≠vel acessar o Sistema Usu√°rio ou senha incorretos tente novamente", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            Login tela = new Login();
            Painel tela1 = new Painel();
            tela.setVisible(false);
            tela1.setVisible(true);
        } catch (SQLException | ClassNotFoundException err) {
            JOptionPane.showMessageDialog(null, "N√£o foi opss√≠vel acessar o Login tente novamente", "Erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, err);
        }
    }//GEN-LAST:event_ButtonLoginMouseClicked

