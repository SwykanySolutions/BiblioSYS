    private void ButtonCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButtonCadastrarMouseClicked
        // TODO add your handling code here:
        if(!ValidarCampos()){
            return;
        }

        String tipo = null;

        switch(SelectTipo.getSelectedIndex()){
            case 1 -> tipo = "Rua";
            case 2 -> tipo = "Avenida";
            case 3 -> tipo = "Estrada";
            default -> {
            }
        }

        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String data = format.format(InputDataDeNascimento.getDate());
        Date DataDeNascimento = Date.valueOf(data);
        String nome_completo = InputNomeCompleto.getText(),
        completo = InputComplemento.getText(),
        telefone = InputTelefone.getText(),
        cpf = InputCpf.getText(),
        endereco = InputEndereco.getText();
        Integer numero = Integer.valueOf(InputNumero.getText());
        try {
            PessoaDTO.cadastrar(
                nome_completo,
                DataDeNascimento,
                telefone,
                cpf, tipo,
                endereco,
                tipo,
                completo,
                numero
            );
            InputDataDeNascimento.cleanup();
            InputNomeCompleto.setText("");
            InputTelefone.setText("");
            InputCpf.setText("");
            InputEndereco.setText("");

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(FormPainel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ButtonCadastrarMouseClicked
    
